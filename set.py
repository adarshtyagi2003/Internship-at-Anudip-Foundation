set={"january","february","march","april","may","june"}
print(set)

#add function
set.add("august")

#update function
set.update(["july","august","september","october"])
print(set)

#remove function  used to delete a element
set.remove("january")
print(set)

#pop function does not know which element has to be removed 
set.pop()   
print(set)